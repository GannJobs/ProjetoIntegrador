package Class;


public class Feed {
    
    public int Id;
    public String Eg;
    public String Pt;
    public String Fed;
    
    public int getId() {
        return Id;
    }
    public String getEg() {
        return Eg;
    }
    public String getPt() {
        return Pt;
    }
    public String getFed() {
        return Fed;
    }
    public void setId(int Id) {
        this.Id = Id;
    }
    public void setEg(String Eg) {
        this.Eg = Eg;
    }
    public void setPt(String Pt) {
        this.Pt = Pt;
    }
    public void setFed(String Fed) {
        this.Fed = Fed;
    }   
}